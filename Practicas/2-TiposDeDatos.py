
# String (str)
print(type("Cadena"))
'otra cadena'
'''
String 
largo
'''

# numeros (int)
print(type(10))
-5

# punto flotante (float)
print(type(1.5))
5.0

# boleanos (bool)
print(type(True))
False

# Complejos (complex)
print(type((1 + 1j)))
(4 + 8j)

# listas (list)
print(type([4, "cadena", 10.0]))
[True, False, 'otra cadena']

# tuplas (tuples)
print(type((10, 20, 30, "Cadena")))
(1.5, 2.5, False)

# Diccionario (dict)
print(type({
    "lunes" : 1,
    "martes" : 5,
    "miercoles" : 4
}))