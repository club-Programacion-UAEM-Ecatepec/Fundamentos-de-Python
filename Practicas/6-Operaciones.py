#   operaciones numericas
# suma
print(5 + 5)
# resta
print(5 - 5)
# multiplicacion
print(5 * 5)
# divicion
print(5 / 4)
# divicion entera
print(5 // 4)
# modulo
print(5 % 5)
# potencia
print(5 ** 2)

#   Operaciones Relacionales
# igualdad
print(4 == 4)
print("Hola" == "hola")
print("4" == 4)
# distinto
print(4 != 4)
# menor que
print(4 < 5)
# mayor que
print(4 > 5)
# menor o igual que
print(4 <= 5)
# mayor o igual que
print(4 >= 5)
print("-------------------------------------------")

#   Operadores booleanos
# equivalencias booleanas
print(bool(0))
print(bool(1))

print(bool(""))
print(bool("hola"))

print(bool(.1))
print(bool(0.0))
print("----------------------------------------")

print(bool({}))
print(bool([]))
print(bool([1]))
# and
print(True and True)
# or
print(True or False or False)
# not
print(not True)

